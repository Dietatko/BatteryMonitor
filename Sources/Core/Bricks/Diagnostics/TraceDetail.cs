namespace ImpruvIT.Diagnostics
{
	/// <summary>
	/// A trace detail levels.
	/// </summary>
	public enum TraceDetail
	{
		/// <summary>
		/// Only an identification of the instance.
		/// </summary>
		Identifier,

		/// <summary>
		/// The basic details of the instance.
		/// </summary>
		Details,

		/// <summary>
		/// Debug level details of the instance.
		/// </summary>
		Debug
	}
}
